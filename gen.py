"""Generate gestures from audio and write data files into an output folder.

The viewer (`visualize/output/visualize.html`) is checked in as-is and is NOT
regenerated by this script.

Outputs (default ./visualize/output/):
  - gen_rotation.npz   canonical generation: {poses [N,165], trans [N,3], betas [300], fps}
  - gen_position.npz   viewer-only: {joints [N,55,3], fps}
  - gt_position.npz    viewer-only: {joints [N,55,3], fps}
  - speech.wav         copy of input audio

Open `visualize/output/visualize.html` in Chrome/Firefox and drag the output
folder onto the drop zone to view.

Usage:
    python gen.py                                       # default audio + GT in visualize/input/
    python gen.py --audio path/to.wav --gt path/to.npz --out runs/exp1
"""

